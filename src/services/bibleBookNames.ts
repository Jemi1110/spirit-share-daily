/**
 * Get proper Spanish Bible book name from various identifiers
 */
export function getSpanishBookName(identifier: string): string {
  if (!identifier) return '';

  // Clean the identifier
  const cleanId = identifier.toLowerCase()
    .replace(/\s+/g, '')
    .replace(/[^a-z0-9]/g, '');

  // Try direct lookup
  const spanishName = SPANISH_BIBLE_BOOKS[cleanId as keyof typeof SPANISH_BIBLE_BOOKS];
  if (spanishName) {
    return spanishName;
  }

  // Try partial matches for common patterns
  for (const [key, value] of Object.entries(SPANISH_BIBLE_BOOKS)) {
    if (cleanId.includes(key) || key.includes(cleanId)) {
      return value;
    }
  }

  // If no match found, return the original identifier capitalized
  return identifier.charAt(0).toUpperCase() + identifier.slice(1);
}

